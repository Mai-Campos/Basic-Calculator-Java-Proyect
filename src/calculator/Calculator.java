    private void handleKeyPress(KeyEvent e) {
        char keyChar = e.getKeyChar();
        if (Character.isDigit(keyChar)) {
            screenOutput.setText(screenOutput.getText() + keyChar);
        } else {
            switch (keyChar) {
                case '+':
                    sumBtnActionPerformed(null);
                    break;
                case '-':
                    restBtnActionPerformed(null);
                    break;
                case '*':
                    jButton4ActionPerformed(null);
                    break;
                case '/':
                    divisionBtnActionPerformed(null);
                    break;
                case '=':
                case KeyEvent.VK_ENTER:
                    equalBtnActionPerformed(null);
                    break;
                case KeyEvent.VK_BACK_SPACE:
                    deleteOneNumberBtnActionPerformed(null);
                    break;
                case KeyEvent.VK_DELETE:
                    deleteBtnActionPerformed(null);
                    break;
                case '.':
                    pointBtnActionPerformed(null);
                    break;
            }
        }
    }

