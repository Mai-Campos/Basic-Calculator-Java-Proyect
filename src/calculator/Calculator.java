    public String withoutZero(float result){
        
        String resultString = "";
        
        resultString =Float.toString(result);
        
        if(result%1 == 0){
        
           resultString = resultString.substring(0,resultString.length()-2);
        }
        
        return resultString;
        
    }
    
    public void delete(){
        
        String result = "";
        
        result = screenOutput.getText();
         
        result = result.substring(0,result.length()-1);
         
        screenOutput.setText(result);
    }
    
