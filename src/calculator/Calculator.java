    private void equalBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_equalBtnActionPerformed
        
       try{
           
        secondNumber = Float.parseFloat(screenOutput.getText());
        
        switch(operator){
            
            case "+": screenOutput.setText(withoutZero(firstNumber + secondNumber));
            break;
            case "-": screenOutput.setText(withoutZero(firstNumber - secondNumber));
            break;
            case "*": screenOutput.setText(withoutZero(firstNumber * secondNumber));
            break;
            case "/":
                if(secondNumber == 0){
                    screenOutput.setText("Sintax Error" + ": " + "division by zero is undefined");
                
                }else{
                    screenOutput.setText(withoutZero(firstNumber / secondNumber));
                }
        }
        
       }catch(Exception ex){
           System.out.println("No se introdujo ningun numero o el segundo numero esta vacio");
       }
        
    }//GEN-LAST:event_equalBtnActionPerformed

